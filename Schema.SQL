
CREATE TABLE PARENT (
    parentCNIC  VARCHAR(20) PRIMARY KEY,
    parent      VARCHAR(50) NOT NULL
);

CREATE TABLE STUDENT (
    studentCNIC VARCHAR(20) PRIMARY KEY,
    studentName VARCHAR(50) NOT NULL,
    mobile      VARCHAR(15),
    city        VARCHAR(30),
    parentCNIC  VARCHAR(20),
    FOREIGN KEY (parentCNIC) REFERENCES PARENT(parentCNIC)
);

CREATE TABLE TUTOR (
    tutorCNIC   VARCHAR(20) PRIMARY KEY,
    tutorName   VARCHAR(50) NOT NULL,
    tutorMobile VARCHAR(15)
);

CREATE TABLE SUBJECT (
    subjectID VARCHAR(10) PRIMARY KEY,
    subject   VARCHAR(50) NOT NULL,
    fee       INT
);

CREATE TABLE SESSION_RECORD (
    studentCNIC VARCHAR(20),
    tutorCNIC   VARCHAR(20),
    subjectID   VARCHAR(10),
    sessionDate DATE,
    sessionTime VARCHAR(10),
    FOREIGN KEY (studentCNIC) REFERENCES STUDENT(studentCNIC),
    FOREIGN KEY (tutorCNIC)   REFERENCES TUTOR(tutorCNIC),
    FOREIGN KEY (subjectID)   REFERENCES SUBJECT(subjectID)
);



